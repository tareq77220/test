let teams = [];
let teamScores = {};
let currentTeamIndex = 0;
let timer;
let selectedWords = [];
let currentAlarm = null; 

const alarmSounds = ['commmmmme.mp3', 'one_more_day.mp3'];
const startSound = '0';
const celebrationSound = 'celebration.mp3';

function setupTeams(teamCount) {
    document.getElementById('teamNamesContainer').classList.remove('hidden');
    document.getElementById('teamInputs').innerHTML = "";

    for (let i = 0; i < teamCount; i++) {
        document.getElementById('teamInputs').innerHTML += `
            <div class="team-container">
                <input type="text" placeholder="اسم الفريق ${i + 1}">
            </div>`;
    }

    document.getElementById('teamInputs').innerHTML += `
        <div class="word-count-container">
            <label for="wordCount">عدد البطاقات لكل لعبة:</label>
            <input type="number" id="wordCount" min="1" max="50" value="10">
        </div>`;
}

function startGame() {
    teams = [];
    document.querySelectorAll('#teamInputs input[type="text"]').forEach(input => {
        let teamName = input.value.trim() || `فريق ${teams.length + 1}`;
        teams.push(teamName);
        teamScores[teamName] = 0;
    });

    
    let wordCount = parseInt(document.getElementById('wordCount').value);

    
    const maxCards = 506;

    
    if (wordCount < 2 || wordCount > maxCards) {
        alert(`عدد البطاقات يجب أن يكون بين 2 و ${maxCards}`);
        return;
    }

    
    wordCount = Math.min(wordCount, words.length);

   
    selectedWords = shuffleArray([...words]).slice(0, wordCount);

    console.log("Selected Words:", selectedWords);

    
    if (selectedWords.length === 0) {
        alert("لا يمكن ان يكون عدد البطاقات 0");
        return;
    }

    
    if (selectedWords.length > maxCards) {
        alert(`عدد البطاقات لا يمكن أن يتجاوز ${maxCards}`);
        return;
    }

    document.getElementById('teamSelection').classList.add('hidden');
    document.getElementById('game').classList.remove('hidden');

    playStartSound().then(() => {
        nextWord();
    });
}

function shuffleArray(array) {
    let shuffled = [...array];
    for (let i = shuffled.length - 1; i > 0; i--) {
        let j = Math.floor(Math.random() * (i + 1));
        [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    return shuffled;
}

function nextWord() {
    if (selectedWords.length === 0) {
        declareWinner();
        return;
    }

    let word = selectedWords.pop();
    if (!word) {
        console.error("No word found!");
        return;
    }

    document.getElementById('word').innerText = word.word;
    let forbiddenList = document.getElementById('forbiddenWords');
    forbiddenList.innerHTML = "";

    word.forbidden.forEach(item => {
        let li = document.createElement("li");
        li.innerText = item;
        forbiddenList.appendChild(li);
    });

    document.getElementById('currentTeam').innerText = teams[currentTeamIndex];
    startTimer(60);
}

function correctAnswer() {
    if (currentAlarm) {
        currentAlarm.pause();
        currentAlarm.currentTime = 0;
    }
    teamScores[teams[currentTeamIndex]]++;
    moveToNextTeam();
}

function wrongAnswer() {
    if (currentAlarm) {
        currentAlarm.pause();
        currentAlarm.currentTime = 0;
    }
    moveToNextTeam();
}

function moveToNextTeam() {
    clearInterval(timer);
    currentTeamIndex = (currentTeamIndex + 1) % teams.length;
    nextWord();
}

function declareWinner() {
    let sortedTeams = Object.entries(teamScores).sort((a, b) => b[1] - a[1]);

    let scoreboardHTML = `<div class="scoreboard celebration">
        <h2 class="winner-animation">🏆 النتيجة النهائية 🎉</h2>
        <table>
            <tr><th>المركز</th><th>الفريق</th><th>النقاط</th></tr>`;

    sortedTeams.forEach((team, index) => {
        scoreboardHTML += `<tr><td>${index + 1}</td><td>${team[0]}</td><td>${team[1]}</td></tr>`;
    });

    scoreboardHTML += `</table>
        <button class="replay-button" onclick="location.reload()">🔄 لعب مرة أخرى</button>
    </div>`;

    document.getElementById('winner').innerHTML = scoreboardHTML;
    document.getElementById('winner').classList.remove('hidden');
    document.getElementById('game').classList.add('hidden');

    playCelebrationSound();
    startConfetti();
}

function startTimer(seconds) {
    clearInterval(timer);
    document.getElementById('time').innerText = seconds;

    timer = setInterval(() => {
        seconds--;
        document.getElementById('time').innerText = seconds;

        if (seconds <= 0) {
            clearInterval(timer);
            playRandomAlarm();
        }
    }, 1000);
}

function playRandomAlarm() {
    let randomIndex = Math.floor(Math.random() * alarmSounds.length);
    currentAlarm = new Audio(alarmSounds[randomIndex]); 

    document.getElementById('nextRoundBtn').classList.remove('hidden');

    currentAlarm.play().catch(() => {
        console.log("Audio failed to play");
    });

    document.getElementById('nextRoundBtn').onclick = function () {
        if (currentAlarm) {
            currentAlarm.pause();
            currentAlarm.currentTime = 0;
        }

        document.getElementById('nextRoundBtn').classList.add('hidden');
        moveToNextTeam();
    };
}

function playStartSound() {
    return new Promise((resolve) => {
        let startAudio = new Audio(startSound);
        startAudio.play().then(resolve).catch(resolve);
    });
}

function playCelebrationSound() {
    let celebrationAudio = new Audio(celebrationSound);
    celebrationAudio.play().catch(() => console.log("Celebration sound failed to play"));
}

function startConfetti() {
    console.log("🎊 Starting confetti animation! 🎊");
}
